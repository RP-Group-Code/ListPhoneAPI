package com.yoga.listphone2022.Model;

public class DataModel {
    private int id;
    private String nama, deskripsi, sub_deskripsi, foto;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getDeskripsi() {
        return deskripsi;
    }

    public void setDeskripsi(String deskripsi) {
        this.deskripsi = deskripsi;
    }

    public String getSub_deskripsi() {
        return sub_deskripsi;
    }

    public void setSub_deskripsi(String sub_deskripsi) {
        this.sub_deskripsi = sub_deskripsi;
    }

    public String getFoto() {
        return foto;
    }

    public void setFoto(String foto) {
        this.foto = foto;
    }
}